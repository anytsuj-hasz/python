import math

def poleProstokata(bokA, bokB):
    return bokA * bokB

def poleKwadratu(bokA):
    return bokA * bokA

def poleTrojkata(podstawa, wysokosc):
    return ((podstawa * wysokosc)/2)

def poleTrapezu(podstawaA, podstawaB, wysokosc):
    return (((podstawaA + podstawaB) * wysokosc)/2)

def poleKola(promien):
    return (math.pi * promien ** 2)

